class InterestingNumber {
public:
	string isInteresting(string);
};

string InterestingNumber::isInteresting(string s) {
	int a[11];
	string M = "Not interesting", N = "Interesting";
	for (int i = 0; i < 11; i++) a[i] = 0;
	
	for (int i = 0; i < s.size(); i++) {
		a[s[i] - '0']++;
	}
	
	for (int i = 0; i < 10; i++) {
		if (a[i] == 0 || a[i] == 2) {
			continue;
		}
		return M;
	}
	int d = s.size();
	for (int i = 0; i < 11; i++) a[i] = 0;
	
	for (int i = 0; i < d; i++) {
		int x = s[i] - '0';
		if (a[i] != 1) {
			if (i + x + 1 >= s.size() || s[i + x + 1] != s[i]) {
				return M;
			}
			a[i] = 1, a[i + x + 1] = 1;
			cout << s[i] << " " << s[i+x+1] << endl;
		}
	}
	
	return N;
}


