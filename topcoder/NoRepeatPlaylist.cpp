class NoRepeatPlaylist {
public:
	long long int dp[101][101][101], pp, mm;
	
	long long int solve(int pos, int x, int y)
	{
		if (pos == pp) return y == 0;
		if (dp[pos][x][y] != -1) return dp[pos][x][y];
		
		long long ans = 0;
		
		if (y > 0) ans += solve(pos + 1, x + 1, y - 1) * y;
		if (x > mm) ans += solve(pos + 1, x, y) * (x - mm);
		
		return dp[pos][x][y] = ans % 1000000007;
		
	}
	int numPlaylists(int, int, int);
};

int NoRepeatPlaylist::numPlaylists(int n, int m, int p) {
	memset(dp, -1, sizeof(dp));
	pp = p;
	mm = m;
	return solve(0, 0, n);
}


