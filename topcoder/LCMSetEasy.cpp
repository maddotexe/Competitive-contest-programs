class LCMSetEasy {
public:
	long long int lcm (long long a, long long b)
	{
		return (a * b / __gcd(a, b));
	}
	string include(vector <int>, int);
};

string LCMSetEasy::include(vector <int> v, int x) {
	long long int t = 1;
	for (int i = 0; i < v.size(); i++) {
		if (x % v[i] == 0) {
			 t = lcm(t, v[i]);
			 if (t > x) return "Impossible";
		}
	}
	if (t == x) return "Possible";
	return "Impossible";
}


