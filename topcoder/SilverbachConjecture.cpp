class SilverbachConjecture {
public:
	vector <int> solve(int);
};

vector <int> SilverbachConjecture::solve(int n) {
	for (int i = 2; i < n; i++) {
		int x = i;
		int y = n - i;
		if (x == 1 || y == 1 || x == n || y == n) continue;
		bool f = false;
		for (int j = 2; j * j <= x; j++) {
			if (x % j == 0) {
				f = true;
			}
		}
		
		if (f) {
			f = false;
			for (int j = 2; j * j <= x; j++) {
				if (y % j == 0) {
					f = true;
				}
			}
			
			if (f) {
				vector <int> v;
				v.push_back(x);
				v.push_back(y);
				return v;
			}
		} else {
			continue;		
		}
	}
}


