class PairGameEasy {
public:
	string able(int, int, int, int);
};

string PairGameEasy::able(int a, int b, int c, int d) {
	while (c > 0 && d > 0) {
	
		if (a == c && b == d) return "Able to generate";
		if (c > d) c -=d;
		else d -= c;
	}
	
	return "Not able to generate";
}


