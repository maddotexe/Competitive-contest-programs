class FibonacciDiv2 {
public:
	int find(int);
};

int FibonacciDiv2::find(int n) {
	vector <int> v;
	int a = 0, b = 1;
	v.push_back(0);
	v.push_back(1);
	while (a + b <= 1000001) {
		v.push_back(b);
		int c = a + b;
		a = b;
		b = c;
	}
	
	v.push_back(b);
	
	int h = 0;
	
	for (int i = 0; i < v.size(); i++) {
		if (v[i] > n) break;
		h = v[i];
	}
	int g = v[v.size() - 1];
	for (int i = v.size() - 1; i >= 0; i--) {
		if (v[i] < n) break;
		g = v[i];
	}
	
	cout << h << " " << g << endl;
	return min(abs(n - h), abs(g - n));
	
	
		
}


