class BoxesDiv2 {
public:
	int findSize(vector <int>);
};

int BoxesDiv2::findSize(vector <int> v) {
	int p2[33], sum = 0;;
	p2[0] = 1;	
	for (int i = 1; i < 33; i++) {
		p2[i] = p2[i-1] * 2;
	}
	
	for (int i = 0; i < v.size(); i++) {
		int pos = 0;
		while (p2[pos] < v[i]) pos++;
		sum += p2[pos];
	}
	
		
	int pos = 0;
	
	while(p2[pos] < sum) pos++;
	
	return p2[pos];
}


