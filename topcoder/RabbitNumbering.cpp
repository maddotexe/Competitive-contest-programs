class RabbitNumbering {
public:
	int theCount(vector <int>);
};

int RabbitNumbering::theCount(vector <int> v) {
	sort(v.begin(), v.end());
	long long sum = v[0];
	for (int i = 1; i < v.size(); i++) {
		sum = (sum * (v[i] - i)) % 1000000007;
	}
	return (int)sum;
}


