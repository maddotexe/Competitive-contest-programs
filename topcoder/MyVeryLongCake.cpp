class MyVeryLongCake {
public:
	int cut(int);
};

int MyVeryLongCake::cut(int n) {
	int res = n, m = n;
	
	if (n % 2 == 0) {
		res /= 2;
		while (n % 2 == 0) n /= 2;
	}
	
	for (int i = 3; i * i <= n; i += 2) {
		if (n % i == 0) {
			res -= res / i;
			while (n % i == 0) n /= i;
		}
	}
	
	if (n > 1) {
		res -= res / n;
	}
	
	return m - res;
}


