long long int gcd(long long int a, long long int b)
{
	if(b == 0) return a;
	else return gcd(b, a%b);
}

int main()
{
	int t;
	cin >> t;
	while(t--) {
		long long int n;
 
		cin >> n;
		long long int sum = 0;
		for(long long int k = 1; k <= sqrt(n); k++) {
			sum += (n/k)-(k-1);
		}
		
		sum = sum * 2;
		
		//cout << sum << endl;
		sum = sum - (int)sqrt(n);
		long long int sq = n*n;
		long long gc = gcd(sq, sum);
		cout << sum/gc << "/" << sq/gc << endl;
	}
}
